/**
 * There are 4 visibility modifiers in Kotlin
 * public -> default and everywhere available
 *
 * Top level declarations:
 * private -> available inside file containing declaration
 * internal -> anywhere in the same module // maven moudule, gradle module, intelliJ module, Android Studio module ...
 *
 * Classes:
 * private -> only available to class members
 * protected -> same as private and subclasses
 * internal -> any client inside module
 * */